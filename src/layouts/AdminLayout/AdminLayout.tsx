import React, { useEffect, useState } from "react";
import { Outlet, Link, useNavigate, useLocation } from "react-router-dom";
import { Logo } from "../../Components/Icons";
import { getAdminProfile } from "../../services/authService";
import { Toaster } from "react-hot-toast";

const AdminLayout: React.FC = () => {
  const navigate = useNavigate();
  const location = useLocation();
  const [isLoading, setIsLoading] = useState(true);
  const [isAuthenticated, setIsAuthenticated] = useState(false);

  const isLoginPage =
    location.pathname === "/admin" || location.pathname === "/admin/";

  useEffect(() => {
    const checkAuth = async () => {
      const token = localStorage.getItem("adminToken");

      if (!token) {
        setIsAuthenticated(false);
        setIsLoading(false);
        if (!isLoginPage) {
          navigate("/admin");
        }
        return;
      }

      try {
        await getAdminProfile();
        setIsAuthenticated(true);
        if (isLoginPage) {
          navigate("/admin/dashboard");
        }
      } catch (error) {
        localStorage.removeItem("adminToken");
        setIsAuthenticated(false);
        if (!isLoginPage) {
          navigate("/admin");
        }
      } finally {
        setIsLoading(false);
      }
    };

    checkAuth();
  }, [navigate, isLoginPage, location.pathname]);

  const handleLogout = () => {
    localStorage.removeItem("adminToken");
    navigate("/admin");
  };

  const menuItems = [
    { title: "الاحصائيات", path: "/admin/dashboard", icon: <StatsMenuIcon /> },
    { title: "المقالات", path: "/admin/articles", icon: <ArticlesMenuIcon /> },
    { title: "المدونة", path: "/admin/blog", icon: <BlogMenuIcon /> },
    { title: "الكتب", path: "/admin/books", icon: <BooksMenuIcon /> },
    { title: "المسموعات", path: "/admin/podcasts", icon: <AudioMenuIcon /> },
    { title: "الاعدادات", path: "/admin/settings", icon: <SettingsMenuIcon /> },
  ];

  if (isLoading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-[#E8EDEF]">
        <div className="text-primary text-lg">جاري التحميل...</div>
      </div>
    );
  }

  if (isLoginPage && !isAuthenticated) {
    return (
      <div className="min-h-screen bg-gray-100" dir="rtl">
        <Outlet />
      </div>
    );
  }

  if (!isAuthenticated) {
    return null;
  }

  return (
    <div
      className="min-h-screen bg-[#E8EDEF] p-4 md:p-8 xl:p-10 xxl:p-[74px] "
      dir="rtl"
    >
      <Toaster position="bottom-right" />
      {/* Sidebar */}
      <aside className="w-[60px] md:w-[250px] xxl:w-[375px] bg-white flex flex-col rounded-[16px] overflow-hidden fixed top-4 md:top-8 xl:top-10 xxl:top-[74px] bottom-4 md:bottom-8 xl:bottom-10 xxl:bottom-[74px] right-4 md:right-6 lg:right-8 z-10">
        {/* Top blue border */}
        <div className="h-[6px] bg-[#007FFF] w-full flex-shrink-0"></div>

        <div className="p-3 md:p-6 flex flex-col flex-1 overflow-y-auto">
          {/* Logo - hidden on mobile */}
          <div className="mb-12 hidden md:flex justify-center flex-shrink-0">
            <a href="/">
              <Logo variant="dark" />
            </a>
          </div>

          {/* Menu */}
          <nav className="space-y-4 flex-1">
            {menuItems.map((item) => {
              const isActive =
                location.pathname === item.path ||
                location.pathname.startsWith(item.path + "/");
              return (
                <Link
                  key={item.path}
                  to={item.path}
                  className={`flex items-center justify-center justify-start gap-2 p-3 rounded-[8px] transition-colors ${
                    isActive
                      ? "bg-primary text-white"
                      : "text-[#007FFF] hover:bg-gray-50"
                  }`}
                >
                  <span className="w-5 h-5 flex items-center justify-center">
                    {item.icon}
                  </span>
                  <span className="hidden md:inline text-base font-normal">
                    {item.title}
                  </span>
                </Link>
              );
            })}
          </nav>

          {/* Logout */}
          <button
            onClick={handleLogout}
            className="flex items-center justify-center justify-center  hover:text-red-500 cursor-pointer gap-2 px-3 md:px-6 py-3 rounded-[8px] hover:bg-gray-50 transition-colors text-red-700 mt-2 flex-shrink-0"
          >
            <LogoutIcon />
            <span className="hidden md:inline text-base font-normal">
              تسجيل الخروج
            </span>
          </button>
        </div>
      </aside>

      {/* Main Content */}
      <main className="mr-[76px] md:mr-[280px] xxl:mr-[407px] min-h-screen">
        <Outlet />
      </main>
    </div>
  );
};

